#!/usr/bin/env python3
'''
    1. Basic annotations - concat
'''


def concat(str1: str, str2: str) -> str:
    '''
        Function that concat two arguments.

        Arguments:
            str1 (str): A parameter string
            str2 (str): A parameter string

        Return:
            str: Concatenated string
    '''
    return str1 + str2
